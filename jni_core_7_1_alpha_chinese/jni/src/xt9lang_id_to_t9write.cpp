#define LANGUAGE_TABLE_SIZE (sizeof(sLanguageTable)/sizeof(sLanguageTable[0]))

int xt9LanguageID2T9Write(int xt9LangID)
{
    int index = (ET9PLIDMASK & xt9LangID);

    if (0 < index && index <= LANGUAGE_TABLE_SIZE) {
        return sLanguageTable[index];
    }

    return 0;
}

